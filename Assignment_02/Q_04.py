# Triangle Perimeters Prompt the user to enter the lengths of each side of a triangle and then calculate and print the perimeter of the triangle (the sum of all of the side lengths).

peram_01 = float(input("What is the length of side 1?"))
peram_02 = float(input("What is the length of side 2?"))
peram_03 = float(input("What is the length of side 3?"))


Triangle_Perimeters =peram_01 + peram_02 + peram_03
print(f'The perimeter of the triangle is {Triangle_Perimeters} ')

